    /// <summary>
    /// Verifies HeadersV1 encoding format matches grpc-go-shmem.
    /// </summary>
    [Test]
    public void HeadersV1_Encoding_MatchesGo()
    {
        var headers = new HeadersV1
        {
            Version = 1,
            HeaderType = 0, // Client headers
            Method = "/test.Service/Method",
            Authority = "localhost",
            DeadlineUnixNano = 0,
            Metadata = new[] { new MetadataKV("custom-key", "value1") }
        };

        var encoded = headers.Encode();

        // Verify format
        Assert.That(encoded[0], Is.EqualTo(1), "Version byte");
        Assert.That(encoded[1], Is.EqualTo(0), "HeaderType byte (client)");

        // Method length at offset 2 (4 bytes little-endian)
        var methodLen = BinaryPrimitives.ReadUInt32LittleEndian(encoded.AsSpan(2, 4));
        Assert.That(methodLen, Is.EqualTo((uint)headers.Method.Length));

        // Method string starts at offset 6
        var methodBytes = Encoding.UTF8.GetString(encoded.AsSpan(6, (int)methodLen));
        Assert.That(methodBytes, Is.EqualTo("/test.Service/Method"));
    }

    /// <summary>
    /// Verifies TrailersV1 encoding format matches grpc-go-shmem.
    /// </summary>
    [Test]
    public void TrailersV1_Encoding_MatchesGo()
    {
        var trailers = new TrailersV1
        {
            Version = 1,
            GrpcStatusCode = Grpc.Core.StatusCode.OK,
            GrpcStatusMessage = "Success",
            Metadata = Array.Empty<MetadataKV>()
        };

        var encoded = trailers.Encode();

        // Verify format
        Assert.That(encoded[0], Is.EqualTo(1), "Version byte");

        // Status code at offset 1 (4 bytes little-endian)
        var statusCode = BinaryPrimitives.ReadInt32LittleEndian(encoded.AsSpan(1, 4));
        Assert.That(statusCode, Is.EqualTo(0), "OK status code");

        // Message length at offset 5 (4 bytes little-endian)
        var msgLen = BinaryPrimitives.ReadUInt32LittleEndian(encoded.AsSpan(5, 4));
        Assert.That(msgLen, Is.EqualTo((uint)"Success".Length));
    }

    /// <summary>
    /// Generates a binary dump file for Go verification.
    /// </summary>
    [Test]
    [Explicit("Run manually to generate binary dump for Go verification")]
    public void GenerateBinaryDumpForGoVerification()
    {
        var dumpPath = Path.Combine(Path.GetTempPath(), "grpc_shm_binary_dump");
        Directory.CreateDirectory(dumpPath);

        // Dump frame header
        var frameHeader = new FrameHeader
        {
            Length = 100,
            StreamId = 1,
            Type = FrameType.Headers,
            Flags = HeadersFlags.Initial
        };
        var frameBytes = new byte[16];
        frameHeader.EncodeTo(frameBytes);
        File.WriteAllBytes(Path.Combine(dumpPath, "frame_header.bin"), frameBytes);

        // Dump ring header
        var ringHeader = new RingHeader
        {
            Capacity = 4096,
            WriteIdx = 0,
            ReadIdx = 0
        };
        var ringBytes = new byte[64];
        var handle = System.Runtime.InteropServices.GCHandle.Alloc(ringBytes, System.Runtime.InteropServices.GCHandleType.Pinned);
        try
        {
            System.Runtime.InteropServices.Marshal.StructureToPtr(ringHeader, handle.AddrOfPinnedObject(), false);
        }
        finally
        {
            handle.Free();
        }
        File.WriteAllBytes(Path.Combine(dumpPath, "ring_header.bin"), ringBytes);

        // Dump headers
        var headers = new HeadersV1
        {
            Version = 1,
            HeaderType = 0,
            Method = "/greet.Greeter/SayHello",
            Authority = "localhost",
            DeadlineUnixNano = 0,
            Metadata = Array.Empty<MetadataKV>()
        };
        File.WriteAllBytes(Path.Combine(dumpPath, "headers_v1.bin"), headers.Encode());

        // Dump trailers
        var trailers = new TrailersV1
        {
            Version = 1,
            GrpcStatusCode = Grpc.Core.StatusCode.OK,
            GrpcStatusMessage = "",
            Metadata = Array.Empty<MetadataKV>()
        };
        File.WriteAllBytes(Path.Combine(dumpPath, "trailers_v1.bin"), trailers.Encode());

        Console.WriteLine($"Binary dumps written to: {dumpPath}");
        Assert.Pass($"Binary dumps generated at {dumpPath}");
    }

    /// <summary>
    /// Cross-language test: Creates a segment in .NET, verifies Go can read it.
    /// </summary>
    [Test]
    public void CrossLanguage_GoCanReadDotNetSegment()
    {
        if (!File.Exists(GoVerifierPath))
        {
            Assert.Ignore($"Go verifier not found at {GoVerifierPath}. Build with: cd GoInterop && {GoVerifierBuildCommand}");
        }

        var segmentName = $"dotnet_test_{Guid.NewGuid():N}";
        using var segment = Segment.Create(segmentName, 4096, 100);

        try
        {
            // Run Go verifier
            var psi = new ProcessStartInfo
            {
                FileName = GoVerifierPath,
                Arguments = $"-name {segmentName}",
                RedirectStandardOutput = true,
                RedirectStandardError = true,
                UseShellExecute = false,
                CreateNoWindow = true
            };

            using var process = Process.Start(psi);
            var output = process!.StandardOutput.ReadToEnd();
            var error = process.StandardError.ReadToEnd();
            process.WaitForExit(5000);

            Console.WriteLine($"Go output:\n{output}");
            if (!string.IsNullOrEmpty(error))
            {
                Console.WriteLine($"Go stderr:\n{error}");
            }

            Assert.That(process.ExitCode, Is.EqualTo(0), $"Go verifier failed with code {process.ExitCode}");
            Assert.That(output, Does.Contain("VERIFY_SUCCESS"), "Go should verify segment successfully");
            Assert.That(output, Does.Contain("Magic: \"GRPCSHM\\x00\""), "Magic should match");
            Assert.That(output, Does.Contain("Version: 1"), "Version should be 1");
            Assert.That(output, Does.Contain("RingA: offset=128, capacity=4096"), "RingA layout should match");
        }
        finally
        {
            // Cleanup is handled by Dispose, but we need to ensure the segment is disposed
            // before checking the file is deleted
        }
    }

    /// <summary>
    /// Cross-language test: Go creates segment, .NET reads it.
    /// </summary>
    [Test]
    public void CrossLanguage_DotNetCanReadGoSegment()
    {
        if (!File.Exists(GoVerifierPath))
        {
            Assert.Ignore($"Go verifier not found at {GoVerifierPath}. Build with: cd GoInterop && {GoVerifierBuildCommand}");
        }

        var segmentName = $"go_test_{Guid.NewGuid():N}";
        var segmentPath = OperatingSystem.IsLinux()
            ? Path.Combine("/dev/shm", $"grpc_shm_{segmentName}")
            : Path.Combine(Path.GetTempPath(), $"grpc_shm_{segmentName}");

        try
        {
            // Have Go create the segment
            var psi = new ProcessStartInfo
            {
                FileName = GoVerifierPath,
                Arguments = $"-name {segmentName} -create -capacity 4096",
                RedirectStandardOutput = true,
                RedirectStandardError = true,
                UseShellExecute = false,
                CreateNoWindow = true
            };

            using var process = Process.Start(psi);
            process!.WaitForExit(5000);
            var output = process.StandardOutput.ReadToEnd();
            var error = process.StandardError.ReadToEnd();

            Assert.That(process.ExitCode, Is.EqualTo(0), $"Go create failed: {error}");
            Console.WriteLine($"Go created segment: {output}");

            // Now open with .NET
            using var segment = Segment.Open(segmentName);

            Assert.That(segment.Header.MagicValue, Is.EqualTo(BitConverter.ToUInt64(ShmConstants.SegmentMagicBytes)), "Magic");
            Assert.That(segment.Header.Version, Is.EqualTo(1u), "Version");
            Assert.That(segment.Header.RingACapacity, Is.EqualTo(4096UL), "RingA capacity");
            Assert.That(segment.Header.RingBCapacity, Is.EqualTo(4096UL), "RingB capacity");
            Assert.That(segment.Header.ServerReady, Is.EqualTo(1u), "ServerReady");
            Assert.That(segment.Header.MaxStreams, Is.EqualTo(100u), "MaxStreams");
        }
        finally
        {
            // Clean up Go-created segment
            try { File.Delete(segmentPath); } catch { }
        }
    }
