    /// <summary>
    /// Verifies .NET can correctly read a segment header generated by Go.
    /// </summary>
    [Test]
    public void GoBinaryCompatibility_SegmentHeader_CanBeRead()
    {
        var goSegmentPath = Path.Combine(GoInteropDir, "go_segment_header.bin");
        if (!File.Exists(goSegmentPath))
        {
            Assert.Ignore($"Go binary dump not found at {goSegmentPath}. Run: cd GoInterop && go run binary_dump.go -output .");
        }

        var bytes = File.ReadAllBytes(goSegmentPath);
        Assert.That(bytes.Length, Is.EqualTo(128), "Segment header should be 128 bytes");

        // Parse header using our structs
        var header = new SegmentHeader();
        var handle = System.Runtime.InteropServices.GCHandle.Alloc(bytes, System.Runtime.InteropServices.GCHandleType.Pinned);
        try
        {
            header = System.Runtime.InteropServices.Marshal.PtrToStructure<SegmentHeader>(handle.AddrOfPinnedObject());
        }
        finally
        {
            handle.Free();
        }

        // Verify magic
        var expectedMagic = BitConverter.ToUInt64(ShmConstants.SegmentMagicBytes);
        Assert.That(header.MagicValue, Is.EqualTo(expectedMagic), "Magic should be GRPCSHM\\0");
        Assert.That(header.Version, Is.EqualTo(1u), "Version");
        Assert.That(header.TotalSize, Is.EqualTo(0x20000UL), "TotalSize");
        Assert.That(header.RingAOffset, Is.EqualTo(0x80UL), "RingAOffset");
        Assert.That(header.RingACapacity, Is.EqualTo(0x10000UL), "RingACapacity");
        Assert.That(header.ServerPID, Is.EqualTo(1234u), "ServerPID");
        Assert.That(header.ClientPID, Is.EqualTo(5678u), "ClientPID");
        Assert.That(header.ServerReady, Is.EqualTo(1u), "ServerReady");
        Assert.That(header.MaxStreams, Is.EqualTo(100u), "MaxStreams");
    }

